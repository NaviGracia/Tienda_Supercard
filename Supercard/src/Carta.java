/**
 * Carta
 */
public class Carta{
    private int n_carta;
    private String nombre;
    private String categoria;
    private double precio;
    private int stock;
    
    public Carta(int n_carta, String nombre, String categoria, double precio, int stock) {
        this.n_carta = n_carta;
        this.nombre = nombre;
        this.categoria = categoria;
        this.precio = precio;
        this.stock = stock;
    }
    
    
    
}